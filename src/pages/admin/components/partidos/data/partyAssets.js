/**
 * helper para obtener todos los assets de un partido
 * @param {string} logoKey - La clave del logo (ej: "ACCION POPULAR")
 * @param {string} candidatoKey - La clave del candidato (ej: "K_FUJIMORI")
 * @returns {object} { logo, candidato }
 */
export const getPartyAssets = (logoKey, candidatoKey = "DEFAULT_CANDIDATE") => {
  return {
    logo: logos[logoKey] || null, // Retorna null si no encuentra el logo
    candidato: candidatos[candidatoKey] || candidatos["DEFAULT_CANDIDATE"],
  };
};

export default { logos, candidatos, getPartyAssets };
